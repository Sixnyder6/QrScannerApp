package com.example.qrscannerapp.performance

/**
 * Определяет классификацию производительности устройства
 * для адаптации функционала приложения.
 */
enum class PerformanceClass {
    /** Высокая производительность. Доступны все функции и сложная графика. */
    HIGH,

    /** Низкая производительность. Используются упрощенная графика и анимации. */
    LOW,

    /** Графика и анимации полностью отключены для максимальной экономии ресурсов. */
    NONE
}